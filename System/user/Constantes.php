	    return "<title>";
	 }
 	
 	 function fncCerrarTitle(){
	    return " </title>";
	 }
	
	 function fncAbrirBody(){
	    return "<body>";
	 }
 	 function fncCerrarBody(){
	    return "</body>";
	 }
	 	 
  }

